const FloatingTechIcons = ({ className = '' }) => {
  const containerRef = useRef(null);
  const iconsRef = useRef([]);

  useEffect(() => {
    if (!containerRef.current) return;
    
    const icons = iconsRef.current;

    // Create floating animations for each icon
    icons.forEach((icon, index) => {
      if (!icon) return;
      
      const delay = index * 0.3;
      const duration = 3 + Math.random() * 2;
      const yMove = 15 + Math.random() * 10;
      const rotation = 5 + Math.random() * 10;

      // Main floating animation
      gsap.to(icon, {
        y: yMove,
        rotation: rotation,
        duration: duration,
        ease: 'sine.inOut',
        repeat: -1,
        yoyo: true,
        delay: delay
      });

      // Subtle scale pulse
      gsap.to(icon, {
        scale: 1.05,
        duration: duration * 0.7,
        ease: 'sine.inOut',
        repeat: -1,
        yoyo: true,
        delay: delay + 0.5
      });
    });

    return () => {
      icons.forEach((icon) => {
        if (icon) gsap.killTweensOf(icon);
      });
    };
  }, []);

  return (
    <div 
      ref={containerRef}
      className={`relative flex flex-wrap items-center justify-center gap-8 ${className}`}
    >
      {techIcons.map((tech, index) => (
        <div
          key={tech.name}
          ref={(el) => (iconsRef.current[index] = el)}
          className="group relative p-4 rounded-2xl bg-gray-900/50 border border-gray-800/50 backdrop-blur-sm hover:border-cyan-500/50 transition-colors cursor-pointer"
          style={{ transformStyle: 'preserve-3d' }}
        >
          <svg
            viewBox={tech.viewBox}
            className="w-12 h-12 md:w-16 md:h-16 transition-transform group-hover:scale-110"
            fill={tech.color}
          >
            <path d={tech.path} />
          </svg>
          <span className="absolute -bottom-8 left-1/2 -translate-x-1/2 text-xs text-gray-400 opacity-0 group-hover:opacity-100 transition-opacity whitespace-nowrap">
            {tech.name}
          </span>
        </div>
      ))}
    </div>
  );
};

export default FloatingTechIcons;